user_grade = input("What is your Grade (Example; A,B,C,D,F)? ")
if user_grade == "A":
    print("Grade: 4.0")
elif user_grade == "B":
    print("Grade: 3.0")
elif user_grade == "C":
    print("Grade: 2.0")
elif user_grade == "D":
    print("Grade: 1.0")
elif user_grade == "F":
    print("Grade: 0.0")
else:
    print("Error finding your grade.")
